CREATE PROCEDURE [dbo].[SP_Orders_Select_ByTransactionId]
	@TransactionId VARCHAR(155)
AS
SET NOCOUNT ON;
BEGIN
	BEGIN TRY
		BEGIN
			SELECT Name, Price, Quantity FROM Orders WHERE TransactionId = @TransactionId 
		END
	END TRY
		BEGIN CATCH
			SELECT ERROR_MESSAGE() AS ERROR,
				ERROR_NUMBER() AS ERROR_NRO
		END CATCH;
END